public class PrintAddress {
    public static void main(String[] args) {
        // Single print
        System.out.print("Name: Saurav Singh, ");
        System.out.print("City: Gorakhpur, ");
        System.out.print("State: Uttar Pradesh\n");

        // Multiple println
        System.out.println("Name: Saurav Singh");
        System.out.println("City: Gorakhpur");
        System.out.println("State: Uttar Pradesh");
    }
}

